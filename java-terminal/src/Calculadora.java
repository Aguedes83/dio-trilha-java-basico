public class Calculadora {
    /**
     * Este método é utilizado para somar dis numeros inteiros
     * @param numeroUm este é o primeiro numero do método
     * @param numeroDois este é o segundo numero do método
     * @return int o resultado deste métodoé a soma dos dois numeros.
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
